# - [-] - Functions ----
a = c(1,2,3) # store a list of values...
b = c(4,5,6) # store a list of values...
df <- function(x,y) { # simply make a list of variable holders after the function...
  bind = cbind(x,y)
  df = data.frame(bind)
  colnames(df) = c("Var1", "Var2") # in-between the curly brackets start to write...
                                   # out the sequence of code you'd like to run...
                                   # and make sure to replace the objects with the...
                                   # holders you selected to list...
  print(df)
}
df(a,b) # execute the function, just feed the actually objects in place of the holders...
