# - [-] - Time Series Frame ----
x = c(1,2,3) # store a list of values...
y = c(4,5,6) # store a list of values...
z = c("02/27/92", "02/27/92", "01/14/92") # list of dates...
z = as.Date(z, "%m/%d/%y") # 2nd element is the formatting of the date...
df = data.frame(x,y,z) # contain the lists in a frame first...
colnames(df) = c("measure", "age", "date") # set col names...
# as.Date converts the strings to a date format...
ts = xts::xts(df[,-c(3)], order.by = df$date) # create the ts object and order by the date col...
# make sure to remove the date variable from the frame bf creating the ts object. 
print(ts)
